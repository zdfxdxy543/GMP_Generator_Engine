from .models import ControlStructure, LayerSelection, ProgramArtifact, UserRequirement


def build_structure(req: UserRequirement, catalog: ModuleCatalog) -> ControlStructure:
    # Five-layer structure required by report: trajectory, mech, distributor, current, observer.
    layers = [
        LayerSelection("trajectory", catalog.trajectory[0], {"jerk_limit": 0.1}),
        LayerSelection("mech", catalog.mech[0], {"kp": 1.5, "ki": 0.8}),
        LayerSelection("distributor", catalog.distributor[0], {"id_ref": 0.0}),
        LayerSelection("current", catalog.current[0], {"kp": 2.0, "ki": 100.0}),
        LayerSelection("observer", catalog.observer[0], {"lpf_bw": 30.0}),
    ]

    if req.control_mode == "position":
        layers[1] = LayerSelection("mech", "ctl_step_vel_pos_ctrl", {"kp": 2.0, "ki": 1.0})
    if req.scenario == "ev":
        layers[4] = LayerSelection("observer", "ctl_step_pmsm_esmo", {"lpf_bw": 45.0})

    return ControlStructure(layers=layers)



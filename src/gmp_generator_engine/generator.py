def generate_program(req: UserRequirement, catalog: ModuleCatalog) -> ProgramArtifact:
    structure = build_structure(req, catalog)
    files = {
        "implement/common/ctl_main.c": render_ctl_main(structure),
        "implement/common/ctl_main.h": render_ctl_main_h(structure),
        "implement/common/user_main.c": render_user_main_c(),
        "implement/common/user_main.h": render_user_main_h(),
    }
    metadata = {
        "requirement": req.text,
        "scenario": req.scenario,
        "control_mode": req.control_mode,
        "layers": [
            {
                "name": x.name,
                "module": x.module,
                "params": x.params,
            }
            for x in structure.layers
        ],
    }
    return ProgramArtifact(
        source_code=files["implement/common/ctl_main.c"],
        metadata=metadata,
        files=files,
    )


def write_artifacts(artifact: ProgramArtifact, out_dir: Path) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    for rel_path, content in artifact.files.items():
        file_path = out_dir / rel_path
        file_path.parent.mkdir(parents=True, exist_ok=True)
        file_path.write_text(content, encoding="utf-8")

    # Backward-compatible top-level copy for quick preview.
    (out_dir / "ctl_main.c").write_text(artifact.source_code, encoding="utf-8")

    (out_dir / "structure.json").write_text(
        json.dumps(artifact.metadata, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
